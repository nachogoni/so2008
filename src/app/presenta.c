int init_presents(int ppid, int pid, char * parameters)
{
	int i = 0, x = 0;

	clearScreen();

	frame = 0;
	fps = 1;
	
	player = playPresents;

	return 0;
}

int playPresents(void)
{
	int i = 0;
	
	if (tcount++ < fps)
		return 0;

	tcount = 0;

	if (frame >= 50)
	{
		player = NULL;
		showSplashScreen();
		return 0;
	}

	setPosition(0,0);
	printf("\n\n\n\n");
	for(i=0;i<18;i++)
		printf("%s", presenta[frame][i]);
		
	frame++;
		
	return 0;	
}

