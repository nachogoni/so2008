{
	int i = 0;
	
	if (tcount++ < 10)
		return 0;

	tcount = 0;

	if (frame >= 20)
	{
		player = NULL;
		return 0;
	}

	setPosition(0,0);
	printf("\n\n\n\n");
	for(i=0;i<18;i++)
		printf("%s", movie[frame][i]);
		
	frame++;
		
	return 0;	
}

